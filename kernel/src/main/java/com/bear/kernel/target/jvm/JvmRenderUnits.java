package com.bear.kernel.target.jvm;

final class JvmRenderUnits {
    private JvmRenderUnits() {
    }

    static String renderBearValue(String generatedHeader, String packageName) {
        return generatedHeader
            + "package " + packageName + ";\n\n"
            + "import java.util.Collections;\n"
            + "import java.util.LinkedHashMap;\n"
            + "import java.util.Map;\n"
            + "import java.util.TreeMap;\n\n"
            + "public final class BearValue {\n"
            + "    private final Map<String, String> values;\n\n"
            + "    private BearValue(Map<String, String> values) { this.values = Collections.unmodifiableMap(values); }\n"
            + "    public static BearValue empty() { return new BearValue(new LinkedHashMap<>()); }\n"
            + "    public static Builder builder() { return new Builder(); }\n"
            + "    public String get(String key) { return values.get(key); }\n"
            + "    public Map<String, String> asMap() { return values; }\n\n"
            + "    public static final class Builder {\n"
            + "        private final Map<String, String> map = new TreeMap<>();\n"
            + "        public Builder put(String key, String value) { map.put(key, value); return this; }\n"
            + "        public BearValue build() { return new BearValue(new LinkedHashMap<>(map)); }\n"
            + "    }\n"
            + "}\n";
    }

    static String renderInvariantException(String runtimeHeader) {
        return runtimeHeader
            + "package com.bear.generated.runtime;\n\n"
            + "public final class BearInvariantViolationException extends RuntimeException {\n"
            + "    public BearInvariantViolationException(String message) { super(message); }\n"
            + "}\n";
    }

    static String renderContainmentGradleEntrypoint(String generatedHeader) {
        return generatedHeader
            + "import groovy.json.JsonSlurper\n"
            + "import java.security.MessageDigest\n"
            + "import org.gradle.api.tasks.SourceSetContainer\n"
            + "import org.gradle.api.tasks.compile.JavaCompile\n\n"
            + "if (!plugins.hasPlugin('java')) {\n"
            + "    return\n"
            + "}\n\n"
            + "def indexFile = file(\"$rootDir/build/generated/bear/config/containment-required.json\")\n"
            + "if (!indexFile.exists()) {\n"
            + "    return\n"
            + "}\n\n"
            + "def index = new JsonSlurper().parse(indexFile)\n"
            + "def blocks = (index.blocks ?: []).sort { a, b -> String.valueOf(a.blockKey) <=> String.valueOf(b.blockKey) }\n"
            + "def markerDir = file(\"$buildDir/bear/containment\")\n"
            + "def markerHash = MessageDigest.getInstance('SHA-256').digest(indexFile.bytes).encodeHex().toString()\n\n"
            + "def compileJavaTask = tasks.named('compileJava', JavaCompile)\n"
            + "def sourceSets = project.extensions.getByType(SourceSetContainer)\n"
            + "def mainSourceSet = sourceSets.getByName('main')\n"
            + "def testSourceSet = sourceSets.findByName('test')\n"
            + "def markerTasks = []\n"
            + "def sharedCompileTaskNames = []\n\n"
            + "blocks.each { block ->\n"
            + "    def blockKey = String.valueOf(block.blockKey)\n"
            + "    def isSharedUnit = (blockKey == '_shared')\n"
            + "    def safeName = blockKey.replaceAll('[^A-Za-z0-9_]', '_')\n"
            + "    def configName = \"bearAllowedDeps_${safeName}\"\n"
            + "    def compileTaskName = \"compileBearImpl_${safeName}\"\n"
            + "    def markTaskName = \"markBearContainment_${safeName}\"\n"
            + "    def outputDir = isSharedUnit\n"
            + "        ? file(\"$buildDir/bear/shared-classes/_shared\")\n"
            + "        : file(\"$buildDir/bear/impl-classes/${blockKey}\")\n\n"
            + "    def implDir = String.valueOf(block.implDir)\n"
            + "    def legacyImplDir = block.containsKey('legacyImplDir') && block.legacyImplDir != null\n"
            + "        ? String.valueOf(block.legacyImplDir)\n"
            + "        : null\n\n"
            + "    def excludePattern = null\n"
            + "    if (isSharedUnit) {\n"
            + "        excludePattern = implDir.startsWith('src/main/java/')\n"
            + "            ? implDir.substring('src/main/java/'.length()) + '/**/*.java'\n"
            + "            : implDir + '/**/*.java'\n"
            + "    } else {\n"
            + "        excludePattern = implDir.startsWith('src/main/java/')\n"
            + "            ? implDir.substring('src/main/java/'.length()) + '/**/*Impl.java'\n"
            + "            : implDir + '/**/*Impl.java'\n"
            + "    }\n"
            + "    compileJavaTask.configure {\n"
            + "        exclude(excludePattern)\n"
            + "        if (!isSharedUnit && legacyImplDir != null && !legacyImplDir.isBlank()) {\n"
            + "            def legacyImplPattern = legacyImplDir.startsWith('src/main/java/')\n"
            + "                ? legacyImplDir.substring('src/main/java/'.length()) + '/**/*Impl.java'\n"
            + "                : legacyImplDir + '/**/*Impl.java'\n"
            + "            exclude(legacyImplPattern)\n"
            + "        }\n"
            + "    }\n\n"
            + "    def pureConfig = configurations.findByName(configName) ?: configurations.create(configName)\n"
            + "    pureConfig.canBeConsumed = false\n"
            + "    pureConfig.canBeResolved = true\n"
            + "    pureConfig.transitive = false\n"
            + "    (block.allowedDeps ?: []).each { dep ->\n"
            + "        dependencies.add(configName, \"${dep.ga}:${dep.version}\")\n"
            + "    }\n\n"
            + "    tasks.register(compileTaskName, JavaCompile) {\n"
            + "        if (!isSharedUnit) {\n"
            + "            dependsOn(compileJavaTask)\n"
            + "        }\n"
            + "        def sourceTrees = [fileTree(\"$rootDir/${implDir}\") { include '**/*.java' }]\n"
            + "        if (isSharedUnit) {\n"
            + "            def generatedMainDir = file(\"$rootDir/build/generated/bear/src/main/java\")\n"
            + "            if (generatedMainDir.exists()) {\n"
            + "                sourceTrees.add(fileTree(generatedMainDir) { include '**/*.java' })\n"
            + "            }\n"
            + "        }\n"
            + "        if (!isSharedUnit && legacyImplDir != null && !legacyImplDir.isBlank()) {\n"
            + "            sourceTrees.add(fileTree(\"$rootDir/${legacyImplDir}\") { include '*Impl.java' })\n"
            + "        }\n"
            + "        source = files(sourceTrees)\n"
            + "        if (isSharedUnit) {\n"
            + "            classpath = files(mainSourceSet.compileClasspath) + pureConfig\n"
            + "        } else {\n"
            + "            classpath = files(mainSourceSet.output, \"$buildDir/classes/java/main\") + pureConfig\n"
            + "        }\n"
            + "        destinationDirectory.set(outputDir)\n"
            + "        options.encoding = 'UTF-8'\n"
            + "    }\n\n"
            + "    if (isSharedUnit) {\n"
            + "        compileJavaTask.configure {\n"
            + "            dependsOn(compileTaskName)\n"
            + "            classpath = classpath + files(outputDir)\n"
            + "        }\n"
            + "        if (testSourceSet != null) {\n"
            + "            testSourceSet.compileClasspath += files(outputDir)\n"
            + "            testSourceSet.runtimeClasspath += files(outputDir)\n"
            + "        }\n"
            + "    }\n\n"
            + "    tasks.register(markTaskName) {\n"
            + "        dependsOn(compileTaskName)\n"
            + "        doLast {\n"
            + "            markerDir.mkdirs()\n"
            + "            file(\"$buildDir/bear/containment/${blockKey}.applied.marker\").text = \"block=${blockKey}\\nhash=${markerHash}\\n\"\n"
            + "        }\n"
            + "    }\n"
            + "    markerTasks.add(markTaskName)\n"
            + "    if (isSharedUnit) {\n"
            + "        sharedCompileTaskNames.add(compileTaskName)\n"
            + "    }\n"
            + "    mainSourceSet.output.dir([builtBy: compileTaskName], outputDir)\n"
            + "}\n\n"
            + "gradle.taskGraph.afterTask { task, state ->\n"
            + "    if (state.failure != null && sharedCompileTaskNames.contains(task.name)) {\n"
            + "        println(\"BEAR_SHARED_DEPS_VIOLATION|unit=_shared|task=${task.name}\")\n"
            + "    }\n"
            + "}\n\n"
            + "tasks.register('markBearContainment') {\n"
            + "    dependsOn(markerTasks)\n"
            + "    doLast {\n"
            + "        markerDir.mkdirs()\n"
            + "        def keys = blocks.collect { String.valueOf(it.blockKey) }.sort()\n"
            + "        file(\"$buildDir/bear/containment/applied.marker\").text = \"hash=${markerHash}\\nblocks=${keys.join(',')}\\n\"\n"
            + "    }\n"
            + "}\n\n"
            + "tasks.named('classes').configure {\n"
            + "    dependsOn('markBearContainment')\n"
            + "}\n";
    }
}


