package com.bear.kernel.target.node;

import com.bear.kernel.ir.BearIr;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;

public class TypeScriptArtifactGenerator {

    /**
     * Generates Ports.ts file.
     */
    public void generatePorts(BearIr ir, Path outputDir, String blockKey) throws IOException {
        String content = renderPorts(ir, blockKey);
        Path portsFile = outputDir.resolve(blockKey + "Ports.ts");
        writeIfDifferent(portsFile, content);
    }

    /**
     * Generates Logic.ts file.
     */
    public void generateLogic(BearIr ir, Path outputDir, String blockKey) throws IOException {
        String content = renderLogic(ir, blockKey);
        Path logicFile = outputDir.resolve(blockKey + "Logic.ts");
        writeIfDifferent(logicFile, content);
    }

    /**
     * Generates Wrapper.ts file.
     */
    public void generateWrapper(BearIr ir, Path outputDir, String blockKey) throws IOException {
        String content = renderWrapper(ir, blockKey);
        Path wrapperFile = outputDir.resolve(blockKey + "Wrapper.ts");
        writeIfDifferent(wrapperFile, content);
    }

    /**
     * Generates user implementation skeleton (only if file doesn't exist).
     */
    public void generateUserImplSkeleton(BearIr ir, Path outputDir, String blockKey) throws IOException {
        String blockName = TypeScriptLexicalSupport.deriveBlockName(blockKey);
        Path implFile = outputDir.resolve(blockName + "Impl.ts");

        if (!Files.exists(implFile)) {
            String content = renderUserImplSkeleton(ir, blockKey);
            Files.createDirectories(outputDir);
            Files.writeString(implFile, content);
        }
    }

    // Rendering methods

    private String renderPorts(BearIr ir, String blockKey) {
        StringBuilder sb = new StringBuilder();
        sb.append("// Generated by bear compile. DO NOT EDIT.\n\n");

        String blockName = TypeScriptLexicalSupport.deriveBlockName(blockKey);

        if (ir.block().effects() != null && !ir.block().effects().allow().isEmpty()) {
            for (BearIr.EffectPort port : ir.block().effects().allow()) {
                String portName = TypeScriptLexicalSupport.kebabToPascal(port.port());
                sb.append("export interface ").append(portName).append("Port {\n");
                for (String op : port.ops()) {
                    sb.append("  ").append(op).append("(input: BearValue): BearValue;\n");
                }
                sb.append("}\n\n");
            }
        }

        return sb.toString();
    }

    private String renderLogic(BearIr ir, String blockKey) {
        StringBuilder sb = new StringBuilder();
        sb.append("// Generated by bear compile. DO NOT EDIT.\n\n");

        String blockName = TypeScriptLexicalSupport.deriveBlockName(blockKey);

        // Generate request/result types
        for (BearIr.Operation op : ir.block().operations()) {
            String opName = op.name();
            String requestName = blockName + opName + "Request";
            String resultName = blockName + opName + "Result";

            sb.append("export interface ").append(requestName).append(" {\n");
            if (op.contract().inputs() != null) {
                for (BearIr.Field field : op.contract().inputs()) {
                    String tsType = TypeScriptTypeMapper.mapType(field.type());
                    String memberName = TypeScriptLexicalSupport.kebabToCamel(field.name());
                    sb.append("  ").append(memberName).append(": ").append(tsType).append(";\n");
                }
            }
            sb.append("}\n\n");

            sb.append("export interface ").append(resultName).append(" {\n");
            if (op.contract().outputs() != null) {
                for (BearIr.Field field : op.contract().outputs()) {
                    String tsType = TypeScriptTypeMapper.mapType(field.type());
                    String memberName = TypeScriptLexicalSupport.kebabToCamel(field.name());
                    sb.append("  ").append(memberName).append(": ").append(tsType).append(";\n");
                }
            }
            sb.append("}\n\n");
        }

        // Generate logic interface
        sb.append("export interface ").append(blockName).append("Logic {\n");
        for (BearIr.Operation op : ir.block().operations()) {
            String opName = op.name();
            String requestName = blockName + opName + "Request";
            String resultName = blockName + opName + "Result";

            sb.append("  ").append(opName).append("(request: ").append(requestName);
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = TypeScriptLexicalSupport.kebabToPascal(port.port());
                    sb.append(", ").append(portName).append(": ").append(portName).append("Port");
                }
            }
            sb.append("): ").append(resultName).append(";\n");
        }
        sb.append("}\n");

        return sb.toString();
    }

    private String renderWrapper(BearIr ir, String blockKey) {
        StringBuilder sb = new StringBuilder();
        sb.append("// Generated by bear compile. DO NOT EDIT.\n\n");

        String blockName = TypeScriptLexicalSupport.deriveBlockName(blockKey);

        for (BearIr.Operation op : ir.block().operations()) {
            String opName = op.name();
            String wrapperName = blockName + "_" + opName;

            sb.append("export class ").append(wrapperName).append(" {\n");
            sb.append("  constructor(\n");
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = TypeScriptLexicalSupport.kebabToPascal(port.port());
                    sb.append("    private readonly ").append(portName).append(": ").append(portName).append("Port,\n");
                }
            }
            sb.append("    private readonly logic: ").append(blockName).append("Logic\n");
            sb.append("  ) {}\n\n");

            sb.append("  execute(request: ").append(blockName).append(op.name()).append("Request");
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = TypeScriptLexicalSupport.kebabToPascal(port.port());
                    sb.append(", ").append(portName).append(": ").append(portName).append("Port");
                }
            }
            sb.append("): ").append(blockName).append(op.name()).append("Result {\n");
            sb.append("    return this.logic.").append(opName).append("(request");
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = TypeScriptLexicalSupport.kebabToPascal(port.port());
                    sb.append(", ").append(portName);
                }
            }
            sb.append(");\n");
            sb.append("  }\n\n");

            sb.append("  static of(");
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = TypeScriptLexicalSupport.kebabToPascal(port.port());
                    sb.append(portName).append(": ").append(portName).append("Port, ");
                }
            }
            sb.append("): ").append(wrapperName).append(" {\n");
            sb.append("    return new ").append(wrapperName).append("(");
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = TypeScriptLexicalSupport.kebabToPascal(port.port());
                    sb.append(portName).append(", ");
                }
            }
            sb.append("new ").append(blockName).append("Impl());\n");
            sb.append("  }\n");
            sb.append("}\n\n");
        }

        return sb.toString();
    }

    private String renderUserImplSkeleton(BearIr ir, String blockKey) {
        StringBuilder sb = new StringBuilder();
        sb.append("// User implementation - this file is never overwritten by bear\n\n");

        String blockName = TypeScriptLexicalSupport.deriveBlockName(blockKey);

        sb.append("import { ").append(blockName).append("Logic").append(" } from '../").append(blockKey).append("Logic';\n\n");

        sb.append("export class ").append(blockName).append("Impl implements ").append(blockName).append("Logic {\n");

        for (BearIr.Operation op : ir.block().operations()) {
            String opName = op.name();
            String requestName = blockName + opName + "Request";
            String resultName = blockName + opName + "Result";

            sb.append("  ").append(opName).append("(request: ").append(requestName);
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = TypeScriptLexicalSupport.kebabToPascal(port.port());
                    sb.append(", ").append(portName).append(": ").append(portName).append("Port");
                }
            }
            sb.append("): ").append(resultName).append(" {\n");
            sb.append("    // TODO: Implement ").append(opName).append("\n");
            sb.append("    throw new Error('Not implemented');\n");
            sb.append("  }\n\n");
        }

        sb.append("}\n");

        return sb.toString();
    }

    // Helper methods

    private void writeIfDifferent(Path file, String content) throws IOException {
        if (Files.exists(file)) {
            String existing = Files.readString(file);
            if (existing.equals(content)) {
                return;
            }
        }
        Files.createDirectories(file.getParent());
        Files.writeString(file, content, java.nio.file.StandardOpenOption.CREATE,
            java.nio.file.StandardOpenOption.TRUNCATE_EXISTING,
            java.nio.file.StandardOpenOption.SYNC);
    }
}
