    private String defaultLiteral(String javaType) {
        return switch (javaType) {
            case "String" -> "\"\"";
            case "BigDecimal" -> "BigDecimal.ZERO";
            case "Integer" -> "0";
            case "Boolean" -> "Boolean.FALSE";
            default -> "null";
        };
    }

    private String renderSurfaceManifest(BearIr ir) {
        String irHash = sha256Hex(canonicalIrBytes(ir));
        List<BearIr.EffectPort> ports = new ArrayList<>(ir.block().effects().allow());
        ports.sort(Comparator.comparing(BearIr.EffectPort::port));
        List<BearIr.Invariant> invariants = new ArrayList<>();
        if (ir.block().invariants() != null) {
            invariants.addAll(ir.block().invariants());
        }
        invariants.sort(Comparator.comparing(BearIr.Invariant::kind).thenComparing(BearIr.Invariant::field));
        List<BearIr.AllowedDep> allowedDeps = new ArrayList<>();
        if (ir.block().impl() != null && ir.block().impl().allowedDeps() != null) {
            allowedDeps.addAll(ir.block().impl().allowedDeps());
        }
        allowedDeps.sort(Comparator.comparing(BearIr.AllowedDep::maven));

        StringBuilder out = new StringBuilder();
        out.append("{");
        out.append("\"schemaVersion\":\"v1\",");
        out.append("\"surfaceVersion\":3,");
        out.append("\"target\":\"jvm\",");
        out.append("\"block\":\"").append(jsonEscape(ir.block().name())).append("\",");
        out.append("\"irHash\":\"").append(irHash).append("\",");
        out.append("\"generatorVersion\":\"jvm-v1\",");
        out.append("\"capabilities\":[");
        for (int i = 0; i < ports.size(); i++) {
            BearIr.EffectPort port = ports.get(i);
            if (i > 0) {
                out.append(",");
            }
            List<String> ops = new ArrayList<>(port.ops());
            ops.sort(String::compareTo);
            out.append("{\"name\":\"").append(jsonEscape(port.port())).append("\",\"ops\":[");
            for (int j = 0; j < ops.size(); j++) {
                if (j > 0) {
                    out.append(",");
                }
                out.append("\"").append(jsonEscape(ops.get(j))).append("\"");
            }
            out.append("]}");
        }
        out.append("],");
        out.append("\"allowedDeps\":[");
        for (int i = 0; i < allowedDeps.size(); i++) {
            if (i > 0) {
                out.append(",");
            }
            BearIr.AllowedDep dep = allowedDeps.get(i);
            out.append("{\"ga\":\"").append(jsonEscape(dep.maven())).append("\",\"version\":\"")
                .append(jsonEscape(dep.version())).append("\"}");
        }
        out.append("],");
        out.append("\"invariants\":[");
        boolean first = true;
        for (BearIr.Invariant invariant : invariants) {
            if (invariant.kind() != BearIr.InvariantKind.NON_NEGATIVE) {
                continue;
            }
            if (!first) {
                out.append(",");
            }
            first = false;
            out.append("{\"kind\":\"non_negative\",\"field\":\"")
                .append(jsonEscape(invariant.field()))
                .append("\"}");
        }
        out.append("]}");
        out.append("\n");
        return out.toString();
    }

    private String renderWiringManifest(
        String blockKey,
        String generatedPackageName,
        String blockName,
        String implPackageName,
        String implPackagePath,
        List<PortModel> ports
    ) {
        List<String> requiredEffectPorts = new ArrayList<>();
        for (PortModel port : ports) {
            requiredEffectPorts.add(port.variableName);
        }
        requiredEffectPorts.sort(String::compareTo);

        List<String> constructorPortParams = new ArrayList<>();
        for (PortModel port : ports) {
            constructorPortParams.add(port.variableName);
        }

        String entrypointFqcn = generatedPackageName + "." + blockName;
        String logicInterfaceFqcn = generatedPackageName + "." + blockName + "Logic";
        String implFqcn = implPackageName + "." + blockName + "Impl";
        String implSourcePath = "src/main/java/" + implPackagePath + "/" + blockName + "Impl.java";

        StringBuilder out = new StringBuilder();
        out.append("{");
        out.append("\"schemaVersion\":\"v1\",");
        out.append("\"blockKey\":\"").append(jsonEscape(blockKey)).append("\",");
        out.append("\"entrypointFqcn\":\"").append(jsonEscape(entrypointFqcn)).append("\",");
        out.append("\"logicInterfaceFqcn\":\"").append(jsonEscape(logicInterfaceFqcn)).append("\",");
        out.append("\"implFqcn\":\"").append(jsonEscape(implFqcn)).append("\",");
        out.append("\"implSourcePath\":\"").append(jsonEscape(implSourcePath)).append("\",");
        out.append("\"requiredEffectPorts\":[");
        for (int i = 0; i < requiredEffectPorts.size(); i++) {
            if (i > 0) {
                out.append(",");
            }
            out.append("\"").append(jsonEscape(requiredEffectPorts.get(i))).append("\"");
        }
        out.append("],");
        out.append("\"constructorPortParams\":[");
        for (int i = 0; i < constructorPortParams.size(); i++) {
            if (i > 0) {
                out.append(",");
            }
            out.append("\"").append(jsonEscape(constructorPortParams.get(i))).append("\"");
        }
        out.append("]}");
        out.append("\n");
        return out.toString();
    }

    private void writeContainmentArtifacts(
        Path allowedDepsDir,
        Path generatedGradleDir,
        String blockKey,
        String implDir,
        BearIr.Impl impl
    ) throws IOException {
        Files.createDirectories(allowedDepsDir);
        Files.createDirectories(generatedGradleDir);

        Path indexFile = allowedDepsDir.getParent().resolve("containment-required.json");
        Path blockConfig = allowedDepsDir.resolve(blockKey + ".json");
        Map<String, ContainmentBlockSpec> index = readContainmentIndex(indexFile);

        List<ContainmentDep> deps = new ArrayList<>();
        if (impl != null && impl.allowedDeps() != null) {
            for (BearIr.AllowedDep dep : impl.allowedDeps()) {
                deps.add(new ContainmentDep(dep.maven(), dep.version()));
            }
        }
        deps.sort(Comparator.comparing(ContainmentDep::ga));

        if (deps.isEmpty()) {
            index.remove(blockKey);
            deletePath(blockConfig);
        } else {
            ContainmentBlockSpec spec = new ContainmentBlockSpec(blockKey, implDir, null, deps);
            index.put(blockKey, spec);
            write(blockConfig, renderAllowedDepsConfig(spec));
        }

        write(indexFile, renderContainmentRequired(index));
        write(generatedGradleDir.resolve("bear-containment.gradle"), renderContainmentGradleEntrypoint());
    }

    private Map<String, ContainmentBlockSpec> readContainmentIndex(Path indexFile) throws IOException {
        Map<String, ContainmentBlockSpec> index = new TreeMap<>();
        if (!Files.isRegularFile(indexFile)) {
            return index;
        }
        String json = Files.readString(indexFile, StandardCharsets.UTF_8).trim();
        if (json.isEmpty() || !json.startsWith("{") || !json.endsWith("}")) {
            return index;
        }
        String blocksPayload;
        try {
            blocksPayload = extractRequiredArrayPayload(json, "blocks");
        } catch (IllegalStateException e) {
            return index;
        }

        Matcher blockMatcher = Pattern
            .compile(
                "\\{\\\"blockKey\\\":\\\"((?:\\\\.|[^\\\\\\\"])*)\\\",\\\"implDir\\\":\\\"((?:\\\\.|[^\\\\\\\"])*)\\\",(?:\\\"legacyImplDir\\\":\\\"((?:\\\\.|[^\\\\\\\"])*)\\\",)?\\\"allowedDeps\\\":\\[(.*?)\\]\\}",
                Pattern.DOTALL
            )
            .matcher(blocksPayload);
        boolean matchedAny = false;
        while (blockMatcher.find()) {
            matchedAny = true;
            String key = jsonUnescape(blockMatcher.group(1));
            String implDir = jsonUnescape(blockMatcher.group(2));
            String legacyImplDir = blockMatcher.group(3) == null ? null : jsonUnescape(blockMatcher.group(3));
            String depsPayload = blockMatcher.group(4);
            index.put(key, parseContainmentBlock(key, implDir, legacyImplDir, depsPayload));
        }

        if (!matchedAny) {
            Matcher legacyMatcher = Pattern
                .compile(
                    "\\{\\\"blockKey\\\":\\\"((?:\\\\.|[^\\\\\\\"])*)\\\",\\\"legacyImplDir\\\":\\\"((?:\\\\.|[^\\\\\\\"])*)\\\",\\\"allowedDeps\\\":\\[(.*?)\\]\\}",
                    Pattern.DOTALL
                )
                .matcher(blocksPayload);
            while (legacyMatcher.find()) {
                String key = jsonUnescape(legacyMatcher.group(1));
                String legacyImplDir = jsonUnescape(legacyMatcher.group(2));
                String depsPayload = legacyMatcher.group(3);
                String implDir = "src/main/java/blocks/" + key.replace('.', '/') + "/impl";
                index.put(key, parseContainmentBlock(key, implDir, legacyImplDir, depsPayload));
            }
        }
        return index;
    }

    private ContainmentBlockSpec parseContainmentBlock(
        String blockKey,
        String implDir,
        String legacyImplDir,
        String depsPayload
    ) {
        ArrayList<ContainmentDep> deps = new ArrayList<>();
        Matcher depMatcher = Pattern
            .compile("\\{\\\"ga\\\":\\\"((?:\\\\.|[^\\\\\\\"])*)\\\",\\\"version\\\":\\\"((?:\\\\.|[^\\\\\\\"])*)\\\"\\}")
            .matcher(depsPayload);
        while (depMatcher.find()) {
            deps.add(new ContainmentDep(jsonUnescape(depMatcher.group(1)), jsonUnescape(depMatcher.group(2))));
        }
        deps.sort(Comparator.comparing(ContainmentDep::ga));
        return new ContainmentBlockSpec(blockKey, implDir, legacyImplDir, deps);
    }

    private String renderContainmentRequired(Map<String, ContainmentBlockSpec> index) {
        StringBuilder out = new StringBuilder();
        out.append("{");
        out.append("\"schemaVersion\":\"v1\",");
        out.append("\"target\":\"java-gradle\",");
        out.append("\"blocks\":[");
        boolean firstBlock = true;
        for (ContainmentBlockSpec block : index.values()) {
            if (!firstBlock) {
                out.append(",");
            }
            firstBlock = false;
            out.append("{\"blockKey\":\"").append(jsonEscape(block.blockKey())).append("\",");
            out.append("\"implDir\":\"").append(jsonEscape(block.implDir())).append("\",");
            out.append("\"allowedDeps\":[");
            for (int i = 0; i < block.allowedDeps().size(); i++) {
                if (i > 0) {
                    out.append(",");
                }
                ContainmentDep dep = block.allowedDeps().get(i);
                out.append("{\"ga\":\"").append(jsonEscape(dep.ga())).append("\",\"version\":\"")
                    .append(jsonEscape(dep.version())).append("\"}");
            }
            out.append("]}");
        }
        out.append("]}");
        out.append("\n");
        return out.toString();
    }

