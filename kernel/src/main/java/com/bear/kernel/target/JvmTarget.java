    private String renderWiringManifest(
        String blockKey,
        String generatedPackageName,
        String blockName,
        String implPackageName,
        String implPackagePath,
        List<PortModel> ports,
        List<OperationModel> operations,
        boolean hasBlockIdempotency,
        boolean hasBlockInvariants
    ) {
        List<String> requiredEffectPorts = new ArrayList<>();
        for (PortModel port : ports) {
            requiredEffectPorts.add(port.variableName);
        }
        requiredEffectPorts.sort(String::compareTo);
        List<String> logicRequiredPorts = new ArrayList<>();
        for (OperationModel operation : operations) {
            for (PortModel port : operation.logicPorts) {
                logicRequiredPorts.add(port.variableName);
            }
        }
        logicRequiredPorts.sort(String::compareTo);
        logicRequiredPorts = dedupeSortedStrings(logicRequiredPorts);
        List<String> wrapperOwnedSemanticPorts = new ArrayList<>();
        for (OperationModel operation : operations) {
            if (operation.idempotencyStore != null) {
                PortModel semantic = findPort(ports, operation.idempotencyStore.port());
                wrapperOwnedSemanticPorts.add(semantic.variableName);
            }
        }
        wrapperOwnedSemanticPorts.sort(String::compareTo);
        wrapperOwnedSemanticPorts = dedupeSortedStrings(wrapperOwnedSemanticPorts);
        List<String> wrapperOwnedSemanticChecks = new ArrayList<>();
        if (hasBlockIdempotency) {
            wrapperOwnedSemanticChecks.add("IDEMPOTENCY");
        }
        if (hasBlockInvariants) {
            wrapperOwnedSemanticChecks.add("INVARIANTS");
        }

        List<String> constructorPortParams = new ArrayList<>();
        for (PortModel port : ports) {
            constructorPortParams.add(port.variableName);
        }
        return JvmManifestRenderUnits.renderWiringManifest(
            blockKey,
            generatedPackageName,
            blockName,
            implPackageName,
            implPackagePath,
            requiredEffectPorts,
            constructorPortParams,
            logicRequiredPorts,
            wrapperOwnedSemanticPorts,
            wrapperOwnedSemanticChecks
        );
    }

    private void writeContainmentArtifacts(
        Path projectRoot,
        Path allowedDepsDir,
        Path generatedGradleDir,
        String blockKey,
        String implDir,
        BearIr.Impl impl
    ) throws IOException {
        Files.createDirectories(allowedDepsDir);
        Files.createDirectories(generatedGradleDir);

        Path indexFile = allowedDepsDir.getParent().resolve("containment-required.json");
        Path blockConfig = allowedDepsDir.resolve(blockKey + ".json");
        Map<String, ContainmentBlockSpec> index = readContainmentIndex(indexFile);

        List<ContainmentDep> deps = new ArrayList<>();
        if (impl != null && impl.allowedDeps() != null) {
            for (BearIr.AllowedDep dep : impl.allowedDeps()) {
                deps.add(new ContainmentDep(dep.maven(), dep.version()));
            }
        }
        deps.sort(Comparator.comparing(ContainmentDep::ga));

        if (deps.isEmpty()) {
            index.remove(blockKey);
            deletePath(blockConfig);
        } else {
            ContainmentBlockSpec spec = new ContainmentBlockSpec(blockKey, implDir, null, deps);
            index.put(blockKey, spec);
            write(blockConfig, JvmContainmentRenderUnits.renderAllowedDepsConfig(spec));
        }

        Path sharedPolicyPath = projectRoot.resolve(SharedAllowedDepsPolicy.DEFAULT_RELATIVE_PATH);
        boolean sharedInScope = Files.isRegularFile(sharedPolicyPath) || hasSharedJavaSources(projectRoot);
        if (sharedInScope) {
            SharedAllowedDepsPolicyParser policyParser = new SharedAllowedDepsPolicyParser();
            SharedAllowedDepsPolicy sharedPolicy;
            try {
                sharedPolicy = policyParser.parse(sharedPolicyPath);
            } catch (SharedAllowedDepsPolicyException e) {
                throw new IOException("SHARED_POLICY_INVALID: " + e.getMessage(), e);
            }
            ArrayList<ContainmentDep> sharedDeps = new ArrayList<>();
            for (SharedAllowedDepsPolicy.Dependency dep : sharedPolicy.allowedDeps()) {
                sharedDeps.add(new ContainmentDep(dep.maven(), dep.version()));
            }
            sharedDeps.sort(Comparator.comparing(ContainmentDep::ga));
            ContainmentBlockSpec sharedSpec = new ContainmentBlockSpec(SHARED_BLOCK_KEY, SHARED_IMPL_DIR, null, List.copyOf(sharedDeps));
            index.put(SHARED_BLOCK_KEY, sharedSpec);
            write(
                allowedDepsDir.resolve(SHARED_BLOCK_KEY + ".json"),
                JvmContainmentRenderUnits.renderAllowedDepsConfig(sharedSpec)
            );
        } else {
            index.remove(SHARED_BLOCK_KEY);
            deletePath(allowedDepsDir.resolve(SHARED_BLOCK_KEY + ".json"));
        }

        write(indexFile, JvmContainmentRenderUnits.renderContainmentRequired(index));
        write(generatedGradleDir.resolve("bear-containment.gradle"), renderContainmentGradleEntrypoint());
    }

    private Map<String, ContainmentBlockSpec> readContainmentIndex(Path indexFile) throws IOException {
        return new JvmContainmentIndexParser().readContainmentIndex(indexFile);
    }

    private boolean hasSharedJavaSources(Path projectRoot) throws IOException {
        Path sharedRoot = projectRoot.resolve(SHARED_IMPL_DIR);
        if (!Files.isDirectory(sharedRoot)) {
            return false;
        }
        try (var stream = Files.walk(sharedRoot)) {
            return stream
                .filter(Files::isRegularFile)
                .anyMatch(path -> path.getFileName().toString().endsWith(".java"));
        }
    }

