    private String extractRequiredArrayPayload(String json, String key) {
        int keyIdx = json.indexOf("\"" + key + "\":[");
        if (keyIdx < 0) {
            throw new IllegalStateException("missing key: " + key);
        }
        int start = json.indexOf('[', keyIdx);
        if (start < 0) {
            throw new IllegalStateException("malformed array: " + key);
        }
        int depth = 0;
        for (int i = start; i < json.length(); i++) {
            char c = json.charAt(i);
            if (c == '[') {
                depth++;
            } else if (c == ']') {
                depth--;
                if (depth == 0) {
                    return json.substring(start + 1, i);
                }
            }
        }
        throw new IllegalStateException("malformed array: " + key);
    }

    private String jsonUnescape(String value) {
        StringBuilder out = new StringBuilder(value.length());
        for (int i = 0; i < value.length(); i++) {
            char c = value.charAt(i);
            if (c != '\\') {
                out.append(c);
                continue;
            }
            if (i + 1 >= value.length()) {
                break;
            }
            char next = value.charAt(++i);
            switch (next) {
                case 'n' -> out.append('\n');
                case 'r' -> out.append('\r');
                case 't' -> out.append('\t');
                case '\\' -> out.append('\\');
                case '"' -> out.append('"');
                default -> out.append(next);
            }
        }
        return out.toString();
    }

    private byte[] canonicalIrBytes(BearIr ir) {
        BearIrYamlEmitter emitter = new BearIrYamlEmitter();
        String yaml = emitter.toCanonicalYaml(ir);
        return yaml.getBytes(StandardCharsets.UTF_8);
    }

    private String sha256Hex(byte[] bytes) {
        try {
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            byte[] hash = digest.digest(bytes);
            StringBuilder out = new StringBuilder(hash.length * 2);
            for (byte b : hash) {
                out.append(String.format("%02x", b));
            }
            return out.toString();
        } catch (NoSuchAlgorithmException e) {
            throw new IllegalStateException("SHA-256 unavailable", e);
        }
    }

    private String jsonEscape(String value) {
        StringBuilder out = new StringBuilder(value.length() + 8);
        for (int i = 0; i < value.length(); i++) {
            char c = value.charAt(i);
            if (c == '\\' || c == '"') {
                out.append('\\').append(c);
            } else if (c == '\n') {
                out.append("\\n");
            } else if (c == '\r') {
                out.append("\\r");
            } else if (c == '\t') {
                out.append("\\t");
            } else {
                out.append(c);
            }
        }
        return out.toString();
    }

    private List<PortModel> mapPorts(List<BearIr.EffectPort> ports) {
        List<PortModel> models = new ArrayList<>();
        for (BearIr.EffectPort port : ports) {
            List<String> methods = new ArrayList<>();
            for (String op : port.ops()) {
                methods.add(toCamelCase(op));
            }
            methods.sort(String::compareTo);
            String pascalPortName = toPascalCase(port.port());
            String interfaceName = pascalPortName.endsWith("Port")
                ? pascalPortName
                : pascalPortName + "Port";
            models.add(new PortModel(port.port(), interfaceName, toCamelCase(interfaceName), methods));
        }
        models.sort(Comparator.comparing(port -> port.interfaceName));
        return models;
    }

    private List<FieldModel> mapFields(List<BearIr.Field> fields) {
        List<FieldModel> models = new ArrayList<>();
        for (BearIr.Field field : fields) {
            models.add(new FieldModel(field.name(), toCamelCase(field.name()), "get" + toPascalCase(field.name()), toJavaType(field.type())));
        }
        models.sort(Comparator.comparing(field -> field.memberName));
        return models;
    }

    private boolean usesBigDecimal(List<FieldModel> fields) {
        return fields.stream().anyMatch(field -> "BigDecimal".equals(field.javaType));
    }

    private String toJavaType(FieldType type) {
        return switch (type) {
            case STRING -> "String";
            case DECIMAL -> "BigDecimal";
            case INT -> "Integer";
            case BOOL -> "Boolean";
            case ENUM -> "String";
        };
    }

    private PortModel findPort(List<PortModel> ports, String name) {
        return ports.stream().filter(port -> port.originalName.equals(name)).findFirst()
            .orElseThrow(() -> new IllegalStateException("Unknown port: " + name));
    }

    private FieldModel findField(List<FieldModel> fields, String name) {
        return fields.stream().filter(field -> field.originalName.equals(name)).findFirst()
            .orElseThrow(() -> new IllegalStateException("Unknown field: " + name));
    }

    private String sanitizePackageSegment(String raw) {
        List<String> tokens = splitTokens(raw);
        if (tokens.isEmpty()) {
            return "block";
        }
        StringBuilder out = new StringBuilder();
        for (int i = 0; i < tokens.size(); i++) {
            if (i > 0) {
                out.append('.');
            }
            String segment = tokens.get(i);
            if (Character.isDigit(segment.charAt(0))) {
                segment = "_" + segment;
            }
            if (JAVA_KEYWORDS.contains(segment)) {
                segment = segment + "_";
            }
            out.append(segment);
        }
        return out.toString();
    }

    private String sanitizeBlockKey(String raw) {
        List<String> tokens = splitTokens(raw);
        if (tokens.isEmpty()) {
            return "block";
        }
        return String.join("-", tokens);
    }

    private String toPascalCase(String raw) {
        List<String> tokens = splitTokens(raw);
        if (tokens.isEmpty()) {
            return "Block";
        }
        StringBuilder out = new StringBuilder();
        for (String token : tokens) {
            out.append(Character.toUpperCase(token.charAt(0))).append(token.substring(1));
        }
        String value = out.toString();
        if (Character.isDigit(value.charAt(0))) {
            value = "_" + value;
        }
        if (JAVA_KEYWORDS.contains(value)) {
            value = value + "_";
        }
        return value;
    }

    private String toCamelCase(String raw) {
        List<String> tokens = splitTokens(raw);
        if (tokens.isEmpty()) {
            return "value";
        }
        StringBuilder out = new StringBuilder(tokens.get(0));
        for (int i = 1; i < tokens.size(); i++) {
            String token = tokens.get(i);
            out.append(Character.toUpperCase(token.charAt(0))).append(token.substring(1));
        }
        String value = out.toString();
        if (Character.isDigit(value.charAt(0))) {
            value = "_" + value;
        }
        if (JAVA_KEYWORDS.contains(value)) {
            value = value + "_";
        }
        return value;
    }

    private List<String> splitTokens(String raw) {
        String adjusted = raw.replaceAll("([a-z0-9])([A-Z])", "$1 $2").replaceAll("[^A-Za-z0-9]+", " ").trim();
        if (adjusted.isEmpty()) {
            return List.of();
        }
        String[] parts = adjusted.split("\\s+");
        List<String> tokens = new ArrayList<>();
        for (String part : parts) {
            tokens.add(part.toLowerCase(Locale.ROOT));
        }
        return tokens;
    }

    private void deleteDirectoryIfExists(Path directory) throws IOException {
        if (!Files.exists(directory)) {
            return;
        }
        try (var stream = Files.walk(directory)) {
            stream.sorted(Comparator.reverseOrder()).forEach(path -> {
                try {
                    deletePath(path);
                } catch (IOException e) {
                    throw new RuntimeException(e);
                }
            });
        } catch (RuntimeException e) {
            if (e.getCause() instanceof IOException io) {
                throw io;
            }
            throw e;
        }
    }

    private void write(Path file, String content) throws IOException {
        runWithFileLockRetry("write", file, () -> {
            Files.createDirectories(file.getParent());
            Files.writeString(file, content, StandardCharsets.UTF_8);
        });
    }

    private void syncDirectory(Path sourceDir, Path targetDir) throws IOException {
        Files.createDirectories(targetDir);

        TreeMap<String, Path> sourceFiles = new TreeMap<>();
        if (Files.isDirectory(sourceDir)) {
            try (var stream = Files.walk(sourceDir)) {
                stream.filter(Files::isRegularFile).forEach(path -> {
                    String rel = sourceDir.relativize(path).toString().replace('\\', '/');
                    sourceFiles.put(rel, path);
                });
            }
        }

        for (Map.Entry<String, Path> entry : sourceFiles.entrySet()) {
            Path target = targetDir.resolve(entry.getKey().replace('/', java.io.File.separatorChar));
            copyFile(entry.getValue(), target);
        }

        List<Path> staleFiles = new ArrayList<>();
        if (Files.isDirectory(targetDir)) {
            try (var stream = Files.walk(targetDir)) {
                stream.filter(Files::isRegularFile).forEach(path -> {
                    String rel = targetDir.relativize(path).toString().replace('\\', '/');
                    if (!sourceFiles.containsKey(rel)) {
                        staleFiles.add(path);
                    }
                });
            }
        }
        staleFiles.sort(Comparator.reverseOrder());
        for (Path stale : staleFiles) {
            deletePath(stale);
        }
        deleteEmptyDirectories(targetDir);
    }

    private void syncFile(Path sourceFile, Path targetFile) throws IOException {
        copyFile(sourceFile, targetFile);
    }

    private void copyFile(Path sourceFile, Path targetFile) throws IOException {
        byte[] content = Files.readAllBytes(sourceFile);
        try {
            runWithFileLockRetry("replace", targetFile, () -> {
                Files.createDirectories(targetFile.getParent());
                Files.copy(sourceFile, targetFile, StandardCopyOption.REPLACE_EXISTING);
            });
        } catch (IOException e) {
            if (!canFallbackToInPlaceRewrite(targetFile, e)) {
                throw e;
            }
            runWithFileLockRetry("rewrite", targetFile, () -> {
                Files.write(
                    targetFile,
                    content,
                    StandardOpenOption.WRITE,
                    StandardOpenOption.TRUNCATE_EXISTING
                );
            });
        }
    }

    private void deletePath(Path path) throws IOException {
        runWithFileLockRetry("delete", path, () -> Files.deleteIfExists(path));
    }

    private void deleteEmptyDirectories(Path root) throws IOException {
        if (!Files.exists(root)) {
            return;
        }
        List<Path> directories = new ArrayList<>();
        try (var stream = Files.walk(root)) {
            stream.filter(Files::isDirectory).forEach(directories::add);
        }
        directories.sort(Comparator.reverseOrder());
        for (Path directory : directories) {
            if (directory.equals(root)) {
                continue;
            }
            try (var listing = Files.list(directory)) {
                if (!listing.findAny().isPresent()) {
                    deletePath(directory);
                }
            }
        }
    }

    private void runWithFileLockRetry(String action, Path path, IoOperation op) throws IOException {
        int maxAttempts = 6;
        int attempt = 0;
        while (true) {
            try {
                maybeInjectLockForTest(action, path);
                op.run();
                return;
            } catch (IOException e) {
                if (!isRetryableFileLockError(e) || attempt >= maxAttempts - 1) {
                    throw toDeterministicLockError(action, path, e);
                }
                attempt++;
                sleepBackoff(attempt);
            }
        }
    }

    private void maybeInjectLockForTest(String action, Path path) throws IOException {
        String needle = System.getProperty("bear.compile.test.lockPathContains");
        if (needle == null || needle.isBlank()) {
            return;
        }
        String configuredAction = System.getProperty("bear.compile.test.lockAction");
        if (configuredAction != null && !configuredAction.isBlank()
            && !configuredAction.equalsIgnoreCase(action)) {
            return;
        }
        String normalizedPath = path.toString().replace('\\', '/');
        String normalizedNeedle = needle.replace('\\', '/');
        if (normalizedPath.contains(normalizedNeedle)) {
            throw new AccessDeniedException(path.toString(), null, "PROJECT_TEST_WINDOWS_FILE_LOCK_SIMULATED");
        }
    }

    private IOException toDeterministicLockError(String action, Path path, IOException cause) {
        if (!isRetryableFileLockError(cause)) {
            return cause;
        }
        String normalized = path.toString().replace('\\', '/');
        return new IOException(
            "WINDOWS_FILE_LOCK: " + action + " blocked at " + normalized
                + " (close file handles and rerun bear compile)",
            cause
        );
    }

    private boolean isRetryableFileLockError(IOException e) {
        if (e instanceof AccessDeniedException) {
            return true;
        }
        if (e instanceof FileSystemException) {
            String reason = ((FileSystemException) e).getReason();
            if (reason != null && looksLikeLockMessage(reason)) {
                return true;
            }
        }
        String message = e.getMessage();
        return message != null && looksLikeLockMessage(message);
    }

    private boolean looksLikeLockMessage(String message) {
        String lower = message.toLowerCase(Locale.ROOT);
        return lower.contains("access is denied")
            || lower.contains("being used by another process")
            || lower.contains("used by another process")
            || lower.contains("file is locked");
    }

    private boolean canFallbackToInPlaceRewrite(Path targetFile, IOException e) {
        return isLockFailureForFallback(e) && Files.isRegularFile(targetFile) && Files.isWritable(targetFile);
    }

    private boolean isLockFailureForFallback(IOException e) {
        if (isRetryableFileLockError(e)) {
            return true;
        }
        String message = e.getMessage();
        return message != null && message.contains("WINDOWS_FILE_LOCK:");
    }

    private void sleepBackoff(int attempt) throws IOException {
        long millis = Math.min(800L, 50L * (1L << Math.min(4, attempt)));
        try {
            Thread.sleep(millis);
        } catch (InterruptedException e) {
            Thread.currentThread().interrupt();
            throw new IOException("INTERRUPTED_DURING_RETRY", e);
        }
    }

