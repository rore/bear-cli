    private String renderBearSharedOwnerAnnotation() {
        StringBuilder s = new StringBuilder();
        s.append(RUNTIME_GENERATED_HEADER)
            .append("package com.bear.generated.runtime;\n\n")
            .append("import java.lang.annotation.ElementType;\n")
            .append("import java.lang.annotation.Retention;\n")
            .append("import java.lang.annotation.RetentionPolicy;\n")
            .append("import java.lang.annotation.Target;\n\n")
            .append("@Retention(RetentionPolicy.SOURCE)\n")
            .append("@Target(ElementType.TYPE)\n")
            .append("public @interface BearSharedOwner {\n")
            .append("    String value();\n")
            .append("}\n");
        return s.toString();
    }

    private String renderModel(String packageName, String className, List<FieldModel> fields) {
        StringBuilder s = new StringBuilder();
        s.append(GENERATED_HEADER).append("package ").append(packageName).append(";\n\n");
        if (usesBigDecimal(fields)) {
            s.append("import java.math.BigDecimal;\n\n");
        }
        s.append("public final class ").append(className).append(" {\n");
        for (FieldModel field : fields) {
            s.append("    private final ").append(field.javaType).append(" ").append(field.memberName).append(";\n");
        }
        s.append("\n    public ").append(className).append("(");
        for (int i = 0; i < fields.size(); i++) {
            if (i > 0) {
                s.append(", ");
            }
            s.append(fields.get(i).javaType).append(" ").append(fields.get(i).memberName);
        }
        s.append(") {\n");
        for (FieldModel field : fields) {
            s.append("        this.").append(field.memberName).append(" = ").append(field.memberName).append(";\n");
        }
        s.append("    }\n\n");
        for (FieldModel field : fields) {
            s.append("    public ").append(field.javaType).append(" ").append(field.getterName).append("() { return ")
                .append(field.memberName).append("; }\n");
        }
        s.append("}\n");
        return s.toString();
    }

    private String renderPort(String packageName, PortModel port) {
        StringBuilder s = new StringBuilder();
        s.append(GENERATED_HEADER).append("package ").append(packageName).append(";\n\n");
        s.append("public interface ").append(port.interfaceName).append(" {\n");
        if (port.kind == BearIr.EffectPortKind.BLOCK) {
            s.append("    BearValue call(BearValue input);\n");
        } else {
            for (String method : port.methods) {
                s.append("    BearValue ").append(method).append("(BearValue input);\n");
            }
        }
        s.append("}\n");
        return s.toString();
    }

    private String renderBlockPortClient(String packageName, String blockName, PortModel port) {
        List<String> ops = new ArrayList<>(port.targetOps);
        ops.sort(String::compareTo);
        Map<String, String> methodNames = canonicalBlockPortMethodNames(ops);

        StringBuilder s = new StringBuilder();
        s.append(GENERATED_HEADER).append("package ").append(packageName).append(";\n\n");
        s.append("import java.lang.reflect.Constructor;\n");
        s.append("import java.lang.reflect.Field;\n");
        s.append("import java.lang.reflect.Method;\n");
        s.append("import java.math.BigDecimal;\n");
        s.append("import java.util.Arrays;\n");
        s.append("import java.util.Comparator;\n\n");
        s.append("public final class ").append(port.blockClientSimpleName).append(" implements ")
            .append(port.interfaceName).append(" {\n");
        for (String op : ops) {
            String fieldName = "wrapper" + JvmLexicalSupport.toPascalCase(op);
            s.append("    private final Object ").append(fieldName).append(";\n");
        }
        s.append("\n    public ").append(port.blockClientSimpleName).append("(");
        for (int i = 0; i < ops.size(); i++) {
            if (i > 0) {
                s.append(", ");
            }
            String fieldName = "wrapper" + JvmLexicalSupport.toPascalCase(ops.get(i));
            s.append("Object ").append(fieldName);
        }
        s.append(") {\n");
        for (String op : ops) {
            String fieldName = "wrapper" + JvmLexicalSupport.toPascalCase(op);
            s.append("        this.").append(fieldName).append(" = ").append(fieldName).append(";\n");
        }
        s.append("    }\n\n");

        s.append("    @Override\n");
        s.append("    public BearValue call(BearValue input) {\n");
        s.append("        String op = input == null ? null : input.get(\"op\");\n");
        s.append("        if (op == null || op.isBlank()) {\n");
        s.append("            throw new IllegalArgumentException(\"BLOCK_PORT_MAP_MISSING_FIELD field=op\");\n");
        s.append("        }\n");
        s.append("        switch (op) {\n");
        for (String op : ops) {
            s.append("            case ").append(JvmLexicalSupport.javaString(op)).append(":\n");
            s.append("                return ").append(methodNames.get(op)).append("(input);\n");
        }
        s.append("            default:\n");
        s.append("                throw new IllegalArgumentException(\"BLOCK_PORT_MAP_UNKNOWN_OP op=\" + op);\n");
        s.append("        }\n");
        s.append("    }\n\n");

        String targetPackageSegment = JvmLexicalSupport.sanitizePackageSegment(port.targetBlock);
        String targetBlockName = JvmLexicalSupport.toPascalCase(port.targetBlock);
        for (String op : ops) {
            String wrapperSimpleName = targetBlockName + "_" + JvmLexicalSupport.toPascalCase(op);
            String requestFqcn = "com.bear.generated." + targetPackageSegment + "." + wrapperSimpleName + "Request";
            String fieldName = "wrapper" + JvmLexicalSupport.toPascalCase(op);
            s.append("    private BearValue ").append(methodNames.get(op)).append("(BearValue input) {\n");
            s.append("        return dispatchToWrapper(").append(fieldName).append(", ")
                .append(JvmLexicalSupport.javaString(requestFqcn)).append(", input);\n");
            s.append("    }\n\n");
        }

        s.append("    private static BearValue dispatchToWrapper(Object wrapper, String requestFqcn, BearValue input) {\n");
        s.append("        try {\n");
        s.append("            Class<?> requestClass = Class.forName(requestFqcn);\n");
        s.append("            Constructor<?> constructor = requestClass.getConstructors()[0];\n");
        s.append("            Class<?>[] parameterTypes = constructor.getParameterTypes();\n");
        s.append("            Field[] fields = requestClass.getDeclaredFields();\n");
        s.append("            Arrays.sort(fields, Comparator.comparing(Field::getName));\n");
        s.append("            if (fields.length != parameterTypes.length) {\n");
        s.append("                throw new IllegalStateException(\"BLOCK_PORT_MAP_INTERNAL request-shape mismatch: \" + requestFqcn);\n");
        s.append("            }\n");
        s.append("            Object[] args = new Object[parameterTypes.length];\n");
        s.append("            for (int i = 0; i < parameterTypes.length; i++) {\n");
        s.append("                args[i] = decodeInputField(input, fields[i].getName(), parameterTypes[i]);\n");
        s.append("            }\n");
        s.append("            Object request = constructor.newInstance(args);\n");
        s.append("            Method execute = wrapper.getClass().getMethod(\"execute\", requestClass);\n");
        s.append("            Object result = execute.invoke(wrapper, request);\n");
        s.append("            return resultToBearValue(result);\n");
        s.append("        } catch (RuntimeException e) {\n");
        s.append("            throw e;\n");
        s.append("        } catch (Exception e) {\n");
        s.append("            throw new IllegalStateException(\"BLOCK_PORT_MAP_INTERNAL dispatch failed\", e);\n");
        s.append("        }\n");
        s.append("    }\n\n");

        s.append("    private static Object decodeInputField(BearValue input, String fieldName, Class<?> type) {\n");
        s.append("        String raw = input == null ? null : input.get(fieldName);\n");
        s.append("        if (raw == null || raw.isBlank()) {\n");
        s.append("            throw new IllegalArgumentException(\"BLOCK_PORT_MAP_MISSING_FIELD field=\" + fieldName);\n");
        s.append("        }\n");
        s.append("        if (type == String.class) {\n");
        s.append("            return raw;\n");
        s.append("        }\n");
        s.append("        if (type == Integer.class || type == int.class) {\n");
        s.append("            try {\n");
        s.append("                return Integer.valueOf(raw);\n");
        s.append("            } catch (NumberFormatException e) {\n");
        s.append("                throw new IllegalArgumentException(\"BLOCK_PORT_MAP_INVALID_INT field=\" + fieldName + \" value=\" + raw);\n");
        s.append("            }\n");
        s.append("        }\n");
        s.append("        if (type == Boolean.class || type == boolean.class) {\n");
        s.append("            if (\"true\".equals(raw)) return Boolean.TRUE;\n");
        s.append("            if (\"false\".equals(raw)) return Boolean.FALSE;\n");
        s.append("            throw new IllegalArgumentException(\"BLOCK_PORT_MAP_INVALID_BOOL field=\" + fieldName + \" value=\" + raw);\n");
        s.append("        }\n");
        s.append("        if (type == BigDecimal.class) {\n");
        s.append("            try {\n");
        s.append("                return new BigDecimal(raw);\n");
        s.append("            } catch (RuntimeException e) {\n");
        s.append("                throw new IllegalArgumentException(\"BLOCK_PORT_MAP_INVALID_DECIMAL field=\" + fieldName + \" value=\" + raw);\n");
        s.append("            }\n");
        s.append("        }\n");
        s.append("        return raw;\n");
        s.append("    }\n\n");

        s.append("    private static BearValue resultToBearValue(Object result) {\n");
        s.append("        BearValue.Builder builder = BearValue.builder();\n");
        s.append("        if (result == null) {\n");
        s.append("            return builder.build();\n");
        s.append("        }\n");
        s.append("        try {\n");
        s.append("            Field[] fields = result.getClass().getDeclaredFields();\n");
        s.append("            Arrays.sort(fields, Comparator.comparing(Field::getName));\n");
        s.append("            for (Field field : fields) {\n");
        s.append("                Method getter = result.getClass().getMethod(\"get\" + toPascal(field.getName()));\n");
        s.append("                Object value = getter.invoke(result);\n");
        s.append("                if (value != null) {\n");
        s.append("                    builder.put(field.getName(), toText(value));\n");
        s.append("                }\n");
        s.append("            }\n");
        s.append("            return builder.build();\n");
        s.append("        } catch (Exception e) {\n");
        s.append("            throw new IllegalStateException(\"BLOCK_PORT_MAP_INTERNAL result mapping failed\", e);\n");
        s.append("        }\n");
        s.append("    }\n\n");

        s.append("    private static String toText(Object value) {\n");
        s.append("        if (value instanceof BigDecimal decimal) return decimal.toString();\n");
        s.append("        if (value instanceof Boolean bool) return bool ? \"true\" : \"false\";\n");
        s.append("        if (value instanceof Integer integer) return Integer.toString(integer);\n");
        s.append("        return String.valueOf(value);\n");
        s.append("    }\n\n");

        s.append("    private static String toPascal(String raw) {\n");
        s.append("        if (raw == null || raw.isEmpty()) return raw;\n");
        s.append("        return Character.toUpperCase(raw.charAt(0)) + raw.substring(1);\n");
        s.append("    }\n");
        s.append("}\n");
        return s.toString();
    }

