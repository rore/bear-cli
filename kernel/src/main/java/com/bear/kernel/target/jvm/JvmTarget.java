    private List<OperationModel> mapOperations(String blockName, List<PortModel> ports, BearIr.Block block) {
        ArrayList<OperationModel> models = new ArrayList<>();
        for (BearIr.Operation operation : block.operations()) {
            String operationPascal = JvmLexicalSupport.toPascalCase(operation.name());
            String wrapperClassName = blockName + "_" + operationPascal;
            String requestClassName = wrapperClassName + "Request";
            String resultClassName = wrapperClassName + "Result";
            String logicMethodName = "execute" + operationPascal;
            List<FieldModel> inputs = mapFields(operation.contract().inputs());
            List<FieldModel> outputs = mapFields(operation.contract().outputs());

            ArrayList<PortModel> usedPorts = new ArrayList<>();
            for (BearIr.EffectPort usedPort : operation.uses().allow()) {
                usedPorts.add(findPort(ports, usedPort.port()));
            }
            usedPorts.sort(Comparator.comparing(port -> port.interfaceName));

            BearIr.IdempotencyStore idempotencyStore = null;
            ArrayList<PortModel> logicPorts = new ArrayList<>(usedPorts);
            if (operation.idempotency() != null
                && operation.idempotency().mode() == BearIr.OperationIdempotencyMode.USE
                && block.idempotency() != null) {
                idempotencyStore = block.idempotency().store();
                PortModel semanticPort = findPort(ports, idempotencyStore.port());
                logicPorts.removeIf(port -> port.originalName.equals(semanticPort.originalName));
            }

            List<BearIr.Invariant> invariants = operation.invariants() == null || operation.invariants().isEmpty()
                ? null
                : List.copyOf(operation.invariants());

            models.add(new OperationModel(
                operation.name(),
                wrapperClassName,
                requestClassName,
                resultClassName,
                logicMethodName,
                inputs,
                outputs,
                List.copyOf(usedPorts),
                List.copyOf(logicPorts),
                operation.idempotency(),
                idempotencyStore,
                invariants
            ));
        }
        models.sort(Comparator.comparing(model -> model.wrapperClassName));
        return List.copyOf(models);
    }

    private List<String> dedupeSortedStrings(List<String> sorted) {
        ArrayList<String> deduped = new ArrayList<>();
        String previous = null;
        for (String value : sorted) {
            if (value.equals(previous)) {
                continue;
            }
            deduped.add(value);
            previous = value;
        }
        return List.copyOf(deduped);
    }

    private List<FieldModel> idempotencyKeyFields(List<FieldModel> inputs, BearIr.OperationIdempotency idempotency) {
        if (idempotency.keyFromInputs() != null && !idempotency.keyFromInputs().isEmpty()) {
            ArrayList<FieldModel> fields = new ArrayList<>();
            for (String field : idempotency.keyFromInputs()) {
                fields.add(findField(inputs, field));
            }
            return List.copyOf(fields);
        }
        return List.of(findField(inputs, idempotency.key()));
    }

    private List<PortModel> mapPorts(List<BearIr.EffectPort> ports, String blockName) {
        List<PortModel> models = new ArrayList<>();
        for (BearIr.EffectPort port : ports) {
            BearIr.EffectPortKind kind = port.kind() == null ? BearIr.EffectPortKind.EXTERNAL : port.kind();
            List<String> methods = new ArrayList<>();
            String targetBlock = null;
            List<String> targetOps = List.of();
            String blockClientSimpleName = null;
            if (kind == BearIr.EffectPortKind.BLOCK) {
                methods.add("call");
                targetBlock = port.targetBlock();
                ArrayList<String> sortedTargetOps = new ArrayList<>();
                if (port.targetOps() != null) {
                    sortedTargetOps.addAll(port.targetOps());
                }
                sortedTargetOps.sort(String::compareTo);
                targetOps = List.copyOf(sortedTargetOps);
                blockClientSimpleName = blockName + "_" + JvmLexicalSupport.toPascalCase(port.port()) + "BlockClient";
            } else {
                if (port.ops() != null) {
                    for (String op : port.ops()) {
                        methods.add(JvmLexicalSupport.toCamelCase(op));
                    }
                }
                methods.sort(String::compareTo);
            }
            String pascalPortName = JvmLexicalSupport.toPascalCase(port.port());
            String interfaceName = pascalPortName.endsWith("Port")
                ? pascalPortName
                : pascalPortName + "Port";
            models.add(new PortModel(
                port.port(),
                interfaceName,
                JvmLexicalSupport.toCamelCase(interfaceName),
                kind,
                methods,
                targetBlock,
                targetOps,
                blockClientSimpleName
            ));
        }
        models.sort(Comparator.comparing(port -> port.interfaceName));
        return models;
    }

    private Map<String, String> canonicalBlockPortMethodNames(List<String> sortedOps) {
        TreeMap<String, String> mapping = new TreeMap<>();
        HashMap<String, Integer> collisions = new HashMap<>();
        for (String op : sortedOps) {
            String base = "dispatch" + JvmLexicalSupport.toPascalCase(op);
            int count = collisions.getOrDefault(base, 0) + 1;
            collisions.put(base, count);
            String methodName = count == 1 ? base : base + "_" + count;
            mapping.put(op, methodName);
        }
        return Map.copyOf(mapping);
    }

    private List<FieldModel> mapFields(List<BearIr.Field> fields) {
        List<FieldModel> models = new ArrayList<>();
        for (BearIr.Field field : fields) {
            models.add(new FieldModel(
                field.name(),
                JvmLexicalSupport.toCamelCase(field.name()),
                "get" + JvmLexicalSupport.toPascalCase(field.name()),
                JvmLexicalSupport.toJavaType(field.type())
            ));
        }
        models.sort(Comparator.comparing(field -> field.memberName));
        return models;
    }

    private boolean usesBigDecimal(List<FieldModel> fields) {
        return fields.stream().anyMatch(field -> "BigDecimal".equals(field.javaType));
    }

    private PortModel findPort(List<PortModel> ports, String name) {
        return ports.stream().filter(port -> port.originalName.equals(name)).findFirst()
            .orElseThrow(() -> new IllegalStateException("Unknown port: " + name));
    }

    private FieldModel findField(List<FieldModel> fields, String name) {
        return fields.stream().filter(field -> field.originalName.equals(name)).findFirst()
            .orElseThrow(() -> new IllegalStateException("Unknown field: " + name));
    }

