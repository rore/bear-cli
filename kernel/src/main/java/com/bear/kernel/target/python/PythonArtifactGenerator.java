package com.bear.kernel.target.python;

import com.bear.kernel.ir.BearIr;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;

/**
 * Generates Python artifacts from BearIr.
 */
public class PythonArtifactGenerator {

    /**
     * Generates *_ports.py file.
     */
    public void generatePorts(BearIr ir, Path outputDir, String blockKey) throws IOException {
        String content = renderPorts(ir, blockKey);
        String blockName = PythonLexicalSupport.deriveBlockName(blockKey);
        Path portsFile = outputDir.resolve(blockName + "_ports.py");
        writeIfDifferent(portsFile, content);
    }

    /**
     * Generates *_logic.py file.
     */
    public void generateLogic(BearIr ir, Path outputDir, String blockKey) throws IOException {
        String content = renderLogic(ir, blockKey);
        String blockName = PythonLexicalSupport.deriveBlockName(blockKey);
        Path logicFile = outputDir.resolve(blockName + "_logic.py");
        writeIfDifferent(logicFile, content);
    }

    /**
     * Generates *_wrapper.py file.
     */
    public void generateWrapper(BearIr ir, Path outputDir, String blockKey) throws IOException {
        String content = renderWrapper(ir, blockKey);
        String blockName = PythonLexicalSupport.deriveBlockName(blockKey);
        Path wrapperFile = outputDir.resolve(blockName + "_wrapper.py");
        writeIfDifferent(wrapperFile, content);
    }

    /**
     * Generates user implementation skeleton (only if file doesn't exist).
     */
    public void generateUserImplSkeleton(BearIr ir, Path outputDir, String blockKey) throws IOException {
        String blockName = PythonLexicalSupport.deriveBlockName(blockKey);
        Path implFile = outputDir.resolve(blockName + "_impl.py");

        if (!Files.exists(implFile)) {
            String content = renderUserImplSkeleton(ir, blockKey);
            Files.createDirectories(outputDir);
            Files.writeString(implFile, content);
        }
    }

    // Rendering methods

    private String renderPorts(BearIr ir, String blockKey) {
        StringBuilder sb = new StringBuilder();
        sb.append("# Generated by bear compile. DO NOT EDIT.\n\n");
        sb.append("from typing import Protocol, Any\n\n");
        sb.append("BearValue = dict[str, Any]\n\n");

        if (ir.block().effects() != null && !ir.block().effects().allow().isEmpty()) {
            for (BearIr.EffectPort port : ir.block().effects().allow()) {
                String portName = PythonLexicalSupport.kebabToPascal(port.port());
                sb.append("class ").append(portName).append("Port(Protocol):\n");
                for (String op : port.ops()) {
                    sb.append("    def ").append(op).append("(self, input: BearValue) -> BearValue: ...\n");
                }
                sb.append("\n");
            }
        }

        return sb.toString();
    }

    private String renderLogic(BearIr ir, String blockKey) {
        StringBuilder sb = new StringBuilder();
        sb.append("# Generated by bear compile. DO NOT EDIT.\n\n");
        sb.append("from typing import Protocol\n");
        sb.append("from dataclasses import dataclass\n");
        
        // Check if we need Decimal import
        boolean needsDecimal = false;
        for (BearIr.Operation op : ir.block().operations()) {
            if (op.contract().inputs() != null) {
                for (BearIr.Field field : op.contract().inputs()) {
                    if (field.type() == BearIr.FieldType.DECIMAL) {
                        needsDecimal = true;
                        break;
                    }
                }
            }
            if (op.contract().outputs() != null) {
                for (BearIr.Field field : op.contract().outputs()) {
                    if (field.type() == BearIr.FieldType.DECIMAL) {
                        needsDecimal = true;
                        break;
                    }
                }
            }
        }
        
        if (needsDecimal) {
            sb.append("from decimal import Decimal\n");
        }
        sb.append("\n");

        String blockName = PythonLexicalSupport.deriveBlockName(blockKey);
        String blockNamePascal = PythonLexicalSupport.kebabToPascal(blockKey);

        // Generate request/result dataclasses
        for (BearIr.Operation op : ir.block().operations()) {
            String opName = op.name();
            String opNamePascal = Character.toUpperCase(opName.charAt(0)) + opName.substring(1);
            String requestName = blockNamePascal + opNamePascal + "Request";
            String resultName = blockNamePascal + opNamePascal + "Result";

            sb.append("@dataclass\n");
            sb.append("class ").append(requestName).append(":\n");
            if (op.contract().inputs() != null && !op.contract().inputs().isEmpty()) {
                for (BearIr.Field field : op.contract().inputs()) {
                    String pythonType = PythonTypeMapper.mapType(field.type());
                    String memberName = PythonLexicalSupport.kebabToSnake(field.name());
                    sb.append("    ").append(memberName).append(": ").append(pythonType).append("\n");
                }
            } else {
                sb.append("    pass\n");
            }
            sb.append("\n");

            sb.append("@dataclass\n");
            sb.append("class ").append(resultName).append(":\n");
            if (op.contract().outputs() != null && !op.contract().outputs().isEmpty()) {
                for (BearIr.Field field : op.contract().outputs()) {
                    String pythonType = PythonTypeMapper.mapType(field.type());
                    String memberName = PythonLexicalSupport.kebabToSnake(field.name());
                    sb.append("    ").append(memberName).append(": ").append(pythonType).append("\n");
                }
            } else {
                sb.append("    pass\n");
            }
            sb.append("\n");
        }

        // Generate logic protocol
        sb.append("class ").append(blockNamePascal).append("Logic(Protocol):\n");
        for (BearIr.Operation op : ir.block().operations()) {
            String opName = op.name();
            String opNamePascal = Character.toUpperCase(opName.charAt(0)) + opName.substring(1);
            String requestName = blockNamePascal + opNamePascal + "Request";
            String resultName = blockNamePascal + opNamePascal + "Result";

            sb.append("    def ").append(opName).append("(self, request: ").append(requestName);
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = PythonLexicalSupport.kebabToPascal(port.port());
                    String portVarName = Character.toLowerCase(portName.charAt(0)) + portName.substring(1);
                    sb.append(", ").append(portVarName).append(": ").append(portName).append("Port");
                }
            }
            sb.append(") -> ").append(resultName).append(": ...\n");
        }
        sb.append("\n");

        return sb.toString();
    }

    private String renderWrapper(BearIr ir, String blockKey) {
        StringBuilder sb = new StringBuilder();
        sb.append("# Generated by bear compile. DO NOT EDIT.\n\n");

        String blockName = PythonLexicalSupport.deriveBlockName(blockKey);
        String blockNamePascal = PythonLexicalSupport.kebabToPascal(blockKey);

        for (BearIr.Operation op : ir.block().operations()) {
            String opName = op.name();
            String opNamePascal = Character.toUpperCase(opName.charAt(0)) + opName.substring(1);
            String wrapperName = blockNamePascal + "_" + opNamePascal;
            String requestName = blockNamePascal + opNamePascal + "Request";
            String resultName = blockNamePascal + opNamePascal + "Result";

            sb.append("class ").append(wrapperName).append(":\n");
            sb.append("    def __init__(self");
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = PythonLexicalSupport.kebabToPascal(port.port());
                    String portVarName = Character.toLowerCase(portName.charAt(0)) + portName.substring(1);
                    sb.append(", ").append(portVarName).append(": ").append(portName).append("Port");
                }
            }
            sb.append(", logic: ").append(blockNamePascal).append("Logic):\n");
            
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = PythonLexicalSupport.kebabToPascal(port.port());
                    String portVarName = Character.toLowerCase(portName.charAt(0)) + portName.substring(1);
                    sb.append("        self._").append(portVarName).append(" = ").append(portVarName).append("\n");
                }
            }
            sb.append("        self._logic = logic\n\n");

            sb.append("    def execute(self, request: ").append(requestName).append(") -> ").append(resultName).append(":\n");
            sb.append("        return self._logic.").append(opName).append("(request");
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = PythonLexicalSupport.kebabToPascal(port.port());
                    String portVarName = Character.toLowerCase(portName.charAt(0)) + portName.substring(1);
                    sb.append(", self._").append(portVarName);
                }
            }
            sb.append(")\n\n");

            sb.append("    @staticmethod\n");
            sb.append("    def of(");
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                boolean first = true;
                for (BearIr.EffectPort port : op.uses().allow()) {
                    if (!first) sb.append(", ");
                    String portName = PythonLexicalSupport.kebabToPascal(port.port());
                    String portVarName = Character.toLowerCase(portName.charAt(0)) + portName.substring(1);
                    sb.append(portVarName).append(": ").append(portName).append("Port");
                    first = false;
                }
            }
            sb.append(") -> '").append(wrapperName).append("':\n");
            sb.append("        from ..impl.").append(blockName).append("_impl import ").append(blockNamePascal).append("Impl\n");
            sb.append("        return ").append(wrapperName).append("(");
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                boolean first = true;
                for (BearIr.EffectPort port : op.uses().allow()) {
                    if (!first) sb.append(", ");
                    String portName = PythonLexicalSupport.kebabToPascal(port.port());
                    String portVarName = Character.toLowerCase(portName.charAt(0)) + portName.substring(1);
                    sb.append(portVarName);
                    first = false;
                }
                sb.append(", ");
            }
            sb.append(blockNamePascal).append("Impl())\n\n");
        }

        return sb.toString();
    }

    private String renderUserImplSkeleton(BearIr ir, String blockKey) {
        StringBuilder sb = new StringBuilder();
        sb.append("# User implementation - this file is never overwritten by bear\n\n");

        String blockName = PythonLexicalSupport.deriveBlockName(blockKey);
        String blockNamePascal = PythonLexicalSupport.kebabToPascal(blockKey);

        sb.append("from build.generated.bear.").append(blockKey).append(".").append(blockName).append("_logic import ").append(blockNamePascal).append("Logic\n\n");

        sb.append("class ").append(blockNamePascal).append("Impl(").append(blockNamePascal).append("Logic):\n");

        for (BearIr.Operation op : ir.block().operations()) {
            String opName = op.name();
            String opNamePascal = Character.toUpperCase(opName.charAt(0)) + opName.substring(1);
            String requestName = blockNamePascal + opNamePascal + "Request";
            String resultName = blockNamePascal + opNamePascal + "Result";

            sb.append("    def ").append(opName).append("(self, request: ").append(requestName);
            if (op.uses() != null && !op.uses().allow().isEmpty()) {
                for (BearIr.EffectPort port : op.uses().allow()) {
                    String portName = PythonLexicalSupport.kebabToPascal(port.port());
                    String portVarName = Character.toLowerCase(portName.charAt(0)) + portName.substring(1);
                    sb.append(", ").append(portVarName).append(": ").append(portName).append("Port");
                }
            }
            sb.append(") -> ").append(resultName).append(":\n");
            sb.append("        # TODO: Implement ").append(opName).append("\n");
            sb.append("        raise NotImplementedError('Not implemented')\n\n");
        }

        return sb.toString();
    }

    // Helper methods

    private void writeIfDifferent(Path file, String content) throws IOException {
        if (Files.exists(file)) {
            String existing = Files.readString(file);
            if (existing.equals(content)) {
                return;
            }
        }
        Files.createDirectories(file.getParent());
        Files.writeString(file, content, java.nio.file.StandardOpenOption.CREATE,
            java.nio.file.StandardOpenOption.TRUNCATE_EXISTING,
            java.nio.file.StandardOpenOption.SYNC);
    }
}
